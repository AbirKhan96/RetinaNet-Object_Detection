from loguru import logger
from pathlib import Path
import os, glob


# ======================================================================================================
# beg: get specific ext files
# ======================================================================================================
def get_files_of(ext, at):
    return glob.glob(os.path.join(at, f"*.{ext}"))
# ======================================================================================================
# end: get specific ext files
# ======================================================================================================


# ======================================================================================================
# beg: get ext from string
# ======================================================================================================
def get_ext(file): return file.split(".")[-1].lower()
# ======================================================================================================
# end: get ext from string
# ======================================================================================================


# ======================================================================================================
# beg: rm dups from data
# ======================================================================================================
def rm_names_with_parenthesis(in_dir):
    for filename in os.listdir(str(in_dir)):
        if '(' in filename:
            logger.debug(f'removing duplicate {filename}')
            os.remove(str(in_dir / filename))
# ======================================================================================================
# end: rm dups from data
# ======================================================================================================